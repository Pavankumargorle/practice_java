public class Binary {
    public static void main(String[] args) {

        //binary number
        int num1 = 0b101;
        System.out.println(num1);

        //hexa
        int num2 = 0x7e;
        System.out.println(num2);

        //we can use underscore between number to avoid confusion
        int num3 = 1000_000_000;
        System.out.println(num3);

        //we can increement the value of characters
        char c = 'b';
        c++;
        System.out.println(c);
    }
    
}
